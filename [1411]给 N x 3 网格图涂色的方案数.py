class Solution:
    def numOfWays(self, n: int) -> int:
        n -= 1
        a = 1
        b = 1
        while n:
            n -= 1
            a, b = 3 * a + 2 * b, 2 * a + 2 * b
            a %= 1000000007
            b %= 1000000007
        return (6 * a + 6 * b) % 1000000007
